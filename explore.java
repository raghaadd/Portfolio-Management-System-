        try {
            int count=0;
            //DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sys","root","");
            Statement stt = con.createStatement();
            String search ="select idhashtag from hashtag where keyword='"+this.TEXT.getText()+"'";
            ResultSet rs=null;
            rs=stt.executeQuery(search);
		
	    int hashtagg=0;
            while(rs.next()){
                
                count=count+1;
		hashtagg=rs.getInt(1);
		

            }
	     Statement stt1 = con.createStatement();
            String search1 ="select idpost from post_hashtag where idhashtag="+hashtagg+"";
            ResultSet rs1=null;
            rs1=stt1.executeQuery(search1);
 	    int idpostt=0;
	    while(rs1.next()){
                
                count=count+1;
		idpostt=rs1.getInt(1);
		

            }
	    Statement stt2 = con.createStatement();
            String search2="select * from post where idpost="+idpostt+"";
            ResultSet rs2=null;
            rs2=stt2.executeQuery(search2);
 	    int idpostt1=0;
	    String contentt="";
	    String descriptionn="";
	    String usernamee="";
	    while(rs2.next()){
                
                count=count+1;
		idpostt1=rs2.getInt(1);
		contentt=rs2.getString(2);
		descriptionn=rs2.getString(3);
		usernamee=rs2.getString(4);
		

            }
            idLabel.setText(""+idpostt1);
     	    contentLabel.setText(contentt);
            usernameLabel.setText(usernamee);
	    descLabel.setText(descriptionn);		

            
            if(count==0){
                JOptionPane.showMessageDialog( TEXT, "the Region doesn't exist");
            }
           con.setAutoCommit(false); 
        }
            
