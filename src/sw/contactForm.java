    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        Operations operations= new Operations();
            String Name = jTextName.getText();
            String Email = jTextEmail.getText();
            String Message = jTextAreaMessage.getText();
            if(operations.checkEmail(Email,this)){
                operations.contact(Name,Email,Message,this);
                jTextName.setText("");
                jTextEmail.setText("");
                jTextAreaMessage.setText("");
                this.dispose();
                new mainpage22(usernameorigin).setVisible(true);
            }
            else{
            JOptionPane.showMessageDialog(this,"you're not registered to this system");
            }
            jTextName.setText("");
                jTextEmail.setText("");
                jTextAreaMessage.setText("");
    }//GEN-LAST:event_btnSubmitActionPerformed

